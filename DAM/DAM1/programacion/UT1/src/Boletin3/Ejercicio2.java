package Boletin3;

public class Ejercicio2 {

	public static void main(String[] args) {
		int miNumero1=4;
		int miNumero2=8;
		
		//Suma
		int miSuma=miNumero1+miNumero2;
		System.out.println("Suma: "+miSuma);
		
		//Resta
		int miResta=miNumero1-miNumero2;
		System.out.println("Resta: "+miResta);
		
		//Producto
		int miProducto=miNumero1*miNumero2;
		System.out.println("Producto: "+miProducto);
		
		//Modulo
		int miModulo=miNumero1%miNumero2;
		System.out.println("Modulo: "+miModulo);
		

	}

}
