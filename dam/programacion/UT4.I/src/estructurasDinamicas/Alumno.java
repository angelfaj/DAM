package estructurasDinamicas;

public class Alumno {
	//Atributos
	private String nombre;
	
	//Constructores
	public Alumno(String nombre) {
		this.nombre = nombre;
	}

	//Metodos
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
}
