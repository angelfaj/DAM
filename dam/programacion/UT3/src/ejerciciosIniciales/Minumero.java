package ejerciciosIniciales;

public class Minumero {
	
	//Atributos
	int numero;
	
	//Constructor
	public Minumero(int num) {
		numero = num;
	}
	
	//Metodos
	public int getDoble() {
		return numero * 2;
	}
	
	public int getTriple() {
		return numero * 3;
	}
	
	public int getCuadruple() {
		return numero * 4;
	}
}
